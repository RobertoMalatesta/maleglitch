            <h1>#malevichishere</h1>
            <p>ax710 and y_a_v_a won the <a class="color1" href="http://www.stedelijk.nl/malevichishere">#malevichishere contest</a> of the Stedelijk Museum Amsterdam in February 2014. This contest was held at the event of the <strong>Kazimir Malevich and the Russian Avant-garde</strong> exhibition. The idea was to send in a creative work, inspired by Kazimir Malevich's art. We sent in this site: <a class="color2" href="/">Maleglitch.net</a>.</p>
